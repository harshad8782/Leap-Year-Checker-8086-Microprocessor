.MODEL SMALL
ASSUME DS:DATA1,CS:CODE1
DATA1 SEGMENT 
MSG DB 0AH,0DH,'ENTER THE YEAR : $' 
NUMBER DB 6,0,6 DUP('$') 
YS DB 0AH,0DH,'YES,IT IS A LEAP YEAR$' 
N DB 0AH,0DH,'NO,IT IS NOT A LEAP YEAR$' 
DATA1 ENDS 
CODE1 SEGMENT 
START:MOV AX,SEG DATA1 
MOV DS,AX 
LEA DX,MSG 
MOV AH,09H 
INT 21H  
LEA DX,NUMBER 
MOV AH,0AH 
INT 21H 
LEA BX,NUMBER+4 
MOV AH,[BX] 
MOV AL,[BX+1] 
AAD 
MOV BL,04H 
DIV BL
AND AH,0FFH 
JZ YES 
LEA DX,N 
MOV AH,09H 
INT 21H 
JMP DOWN 
YES:
LEA DX,YS 
MOV AH,09H 
INT 21H 
DOWN: 
MOV AH,4CH 
INT 21H 
CODE1 ENDS 
END START
